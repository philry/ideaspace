package com.itany.nmms.entity;

public class Sequence {
    private Integer id;
    private String name;//编号的前缀
    private String value;//编号的序列号

    public Sequence() {
    }

    public Sequence(Integer id, String name, String value) {
        this.id = id;
        this.name = name;
        this.value = value;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
